package ch02_변수와타입;

public class Exa05_문자열 {

	public static void main(String[] args) {

		String name = "홍길동";
		String job = "프로그래머";
		System.out.println(name);
		System.out.println(job);
//		왼쪽의 a는 광주광역시를 담아두고 있는 역할을 한다.
//		문자 =>'', 문자열 => "", 'a'=문자, "a"=문자열
//		"안의 내용은 문자열이라고 한다. 문자열 앞에는 String(앞에 문자를 넣을경우)을 붙여야한다.

	}

}
